data1 = 100  # 100 = 64 + 32 + 4 = 0110 0100
data2 = 50   # 50 = 32 + 16 + 2  = 0011 0010
result = data1 ^ data2  # 0110 0100
#                       ^ 0011 0010
#                         ---------
#                         0101 0110 => 64 + 16 + 4 + 2 = 86
print(f"So {data1} ^ {data2} = {result}")